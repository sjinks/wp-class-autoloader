	public static function register()
	{
		static $once = false;

		if (!$once) {
			\spl_autoload_register([__CLASS__, 'autoload']);
			$once = true;
		}
	}

	public static function autoload(string $class)
	{
		$class = \strtolower($class);
		if (isset($map[$class])) {
			require_once $map[$class];
		}
	}
